// ðŸ”¹ Canvas signature
function setupCanvas(canvasId) {
  const canvas = document.getElementById(canvasId);
  if (!canvas) return;
  const ctx = canvas.getContext("2d");
  canvas.width = canvas.parentElement.clientWidth;
  canvas.height = canvas.parentElement.clientHeight;

  let painting = false;

  function startPosition(e) {
    painting = true;
    draw(e);
    e.preventDefault();
  }

  function endPosition(e) {
    painting = false;
    ctx.beginPath();
    e.preventDefault();
  }

  function draw(e) {
    if (!painting) return;
    const rect = canvas.getBoundingClientRect();
    ctx.lineWidth = 2;
    ctx.lineCap = "round";
    ctx.strokeStyle = "#000";

    let clientX, clientY;
    if (e.touches && e.touches.length > 0) {
      clientX = e.touches[0].clientX;
      clientY = e.touches[0].clientY;
    } else {
      clientX = e.clientX;
      clientY = e.clientY;
    }

    ctx.lineTo(clientX - rect.left, clientY - rect.top);
    ctx.stroke();
    ctx.beginPath();
    ctx.moveTo(clientX - rect.left, clientY - rect.top);
    e.preventDefault();
  }

  // Souris
  canvas.addEventListener("mousedown", startPosition);
  canvas.addEventListener("mouseup", endPosition);
  canvas.addEventListener("mouseout", endPosition);
  canvas.addEventListener("mousemove", draw);

  // Tactile
  canvas.addEventListener("touchstart", startPosition);
  canvas.addEventListener("touchend", endPosition);
  canvas.addEventListener("touchcancel", endPosition);
  canvas.addEventListener("touchmove", draw);
}

function clearCanvas(canvasId) {
  const canvas = document.getElementById(canvasId);
  if (!canvas) return;
  const ctx = canvas.getContext("2d");
  ctx.clearRect(0, 0, canvas.width, canvas.height);
}

// ðŸ”¹ Sauvegarde et restauration du contenu
function savePageContent() {
  const page = document.querySelector("#page4");
  if (!page) return;

  const inputs = page.querySelectorAll('input, textarea, select, canvas');

  inputs.forEach(input => {
    if (input.type === 'checkbox' || input.type === 'radio') {
      if (input.checked) {
        input.setAttribute('checked', 'checked');
      } else {
        input.removeAttribute('checked');
      }
    } else if (input.tagName.toLowerCase() === 'textarea') {
      input.textContent = input.value; // âœ… conserve la valeur interne
    } else if (input.tagName.toLowerCase() === 'canvas') {
      // âœ… Sauvegarde la signature sous forme d'image base64
      const dataURL = input.toDataURL();
      input.setAttribute('data-image', dataURL);
    } else {
      input.setAttribute('value', input.value);
    }
  });

  localStorage.setItem("page4Content", page.outerHTML);
}

function loadPageContent() {
const savedContent = localStorage.getItem("page4Content");
  if (!savedContent) return;

  const page = document.querySelector("#page4");
  if (!page) return;

  const parser = new DOMParser();
  const doc = parser.parseFromString(savedContent, "text/html");
  const savedPage = doc.querySelector("#page4");

  if (savedPage) {
    page.innerHTML = savedPage.innerHTML;

    // ðŸ”„ RÃ©initialise les canvases sauvegardÃ©s
    page.querySelectorAll("canvas[data-image]").forEach(canvas => {
      const img = new Image();
      img.onload = () => {
        const ctx = canvas.getContext("2d");
        ctx.drawImage(img, 0, 0);
      };
      img.src = canvas.getAttribute("data-image");
    });
  }
}

